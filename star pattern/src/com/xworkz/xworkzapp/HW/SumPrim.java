package com.xworkz.xworkzapp.HW;

public class SumPrim {
	public static void main(String[] args) {
		int i, j, n = 4;
		int count=1;
		for (i = 0; i < n; i++) {
			for (j = 0; j < n; j++) { 
				//System.out.print(i+""+j+"");
				System.out.print(count[i][j]+" ");
				count++;	
			}System.out.println();
}	
	}
}
